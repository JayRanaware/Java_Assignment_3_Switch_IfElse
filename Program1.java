


class SwitchDemo{

	public static void main(String[] args){
	
	int num=45;


	switch(num%2){
	  
		case 0:
			System.out.println("Number is even");
			break;
	
		case 1:
			System.out.println("Number is odd");
			break;
	
		default :
			System.out.println("Number is zero");
			break;
	}
	
	
	
	}

}
